package com.example.present;

public class LearningDisabilityModel {
    String name;
    int image;

    public LearningDisabilityModel(String name, int image) {
        this.name = name;
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public int getImage() {
        return image;
    }
}
